type CooperativeType = "지역농협" | "축협" | "품목농협";
type CooperativeStatus = "active" | "pending";

type CooperativeRecord = {
  cooperative_id: string;
  cooperative_name: string;
  cooperative_type: CooperativeType;
  sido: string;
  sigungu: string;
  address: string;
  status: CooperativeStatus;
  source: string;
  updated_at: string;
};

const buildCoop = (
  id: number,
  name: string,
  type: CooperativeType,
  sido: string,
  sigungu: string,
  status: CooperativeStatus = "active",
): CooperativeRecord => ({
  cooperative_id: `coop-${String(id).padStart(3, "0")}`,
  cooperative_name: name,
  cooperative_type: type,
  sido,
  sigungu,
  address: [sido, sigungu].filter(Boolean).join(" "),
  status,
  source: "전국 농협 마스터",
  updated_at: "2026.05.01",
});

